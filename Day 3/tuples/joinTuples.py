#To join two or more tuples you can use the + operator:
#ex:
nama_cewek = ('Sarah', 'Amanda', 'Lisa', 'Emily', 'Jessica')
nama_tuan_putri = ('Elizabeth', 'Victoria', 'Catherine', 'Isabella', 'Mary')
nama_perempuan = nama_tuan_putri + nama_cewek
print(nama_perempuan) #output: ('Elizabeth', 'Victoria', 'Catherine', 'Isabella', 'Mary', 'Sarah', 'Amanda', 'Lisa', 'Emily', 'Jessica')

#multiply tuples *
nama_tuan_putri_2 = nama_tuan_putri * 2
print(nama_tuan_putri_2) #output: ('Elizabeth', 'Victoria', 'Catherine', 'Isabella', 'Mary', 'Elizabeth', 'Victoria', 'Catherine', 'Isabella', 'Mary')