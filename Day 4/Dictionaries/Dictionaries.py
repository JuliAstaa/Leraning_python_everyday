# Dictionaries are used to store data values in key:value pairs.
# ex:
people = {
    'name': 'John Doe',
    'age': 23,
    'email': 'johndoe@gmail.com',
    'single': True
}

print(people) #output: {'name': 'John Doe', 'age': 23, 'email': 'johndoe@gmail.com', 'single': True}

# dictionary constructor()
car = dict(brand= 'Ford', model='Mustang', year=1964, colors=['red', 'yellow', 'blue'])
print(car) #output: {'brand': 'Ford', 'model': 'Mustang', 'year': 1964, 'colors': ['red', 'yellow', 'blue']}